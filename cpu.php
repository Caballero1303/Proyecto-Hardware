		<div id="descripcion">

			<div class="wrap">
				<div class="wrap-texto">
					<br>
					<p style="text-align: justify;">
						La CPU actúa como el cerebro de la computadora y realiza la mayor parte del procesamiento de
						datos.
						Ejecuta instrucciones almacenadas en la memoria para realizar operaciones aritméticas, lógicas y
						de
						control.</p>

					<br>

					<p style="text-align: justify;">

						<strong>Componentes Principales</strong>

						<br>
						Unidad de Control (CU): Supervisa y coordina las operaciones en la CPU y controla la ejecución
						de
						instrucciones.
						<br>

						Unidad Aritmético-Lógica (ALU): Realiza operaciones aritméticas y lógicas, como sumas, restas y
						comparaciones.
						<br>

						Registros: Pequeñas áreas de almacenamiento dentro de la CPU utilizadas para almacenar datos
						temporalmente durante las operaciones.
						<br>

						Velocidad de la CPU:
						La velocidad de la CPU se mide en Hertzios (Hz) y representa la cantidad de operaciones que la
						CPU
						puede
						realizar por segundo. Las CPU modernas suelen tener velocidades en el rango de gigahercios (GHz)
						o
						terahercios (THz).
						<br>

						Núcleos y Hilos:
						Las CPU modernas a menudo tienen múltiples núcleos y, en algunos casos, admiten la ejecución de
						múltiples hilos por núcleo. Esto permite que la CPU maneje tareas múltiples de manera más
						eficiente.
						<br>

						Arquitecturas de CPU:
						Hay diferentes arquitecturas de CPU, como x86, x86-64, ARM, etc. Estas arquitecturas determinan
						la
						compatibilidad del software y cómo se ejecutan las instrucciones.
						<br>

						Fabricantes:
						Algunos de los fabricantes más conocidos de CPU incluyen Intel y AMD. Estas empresas diseñan y
						producen
						una variedad de procesadores para una amplia gama de dispositivos, desde computadoras de
						escritorio
						hasta servidores y dispositivos móviles.
						<br>

						Avances Tecnológicos:
						A lo largo del tiempo, las CPU han experimentado mejoras en velocidad, eficiencia energética y
						capacidades de procesamiento. Los avances tecnológicos incluyen la reducción del tamaño de los
						transistores, la incorporación de más núcleos y el desarrollo de arquitecturas más eficientes.
						<br>

						Caché:
						Las CPU también tienen niveles de caché (L1, L2, L3) para almacenar datos e instrucciones
						temporalmente
						y acelerar el acceso a la memoria.
						<br>

						Refrigeración:
						Debido a la generación de calor durante la operación intensiva, las CPU a menudo requieren
						sistemas
						de
						refrigeración, como disipadores de calor y ventiladores.
						<br>

						En resumen, la CPU desempeña un papel crucial en el rendimiento de una computadora, y su
						capacidad y
						velocidad afectan directamente la velocidad y la capacidad de respuesta del sistema.
					</p>
