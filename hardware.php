        <h2>Informacion basica acerca del hardware</h2>
        <div id="descripcion">

            <br>
            <p style="text-align: justify;">
                El hardware puede tener diferentes especificaciones y configuraciones según el uso previsto. Los
                requerimientos para jugar videojuegos de alta gama son distintos a los de realizar tareas básicas de
                oficina. Además, la compatibilidad entre los componentes es crucial para un funcionamiento óptimo del
                sistema.</p>

            <br>

            <p style="text-align: justify;">

                <strong>¿Para que sirve el Hardware?</strong>

                <br>
                Ejecutar programas y procesar datos: La CPU realiza operaciones lógicas y aritméticas, permitiendo
                ejecutar programas y procesar datos de manera eficiente.
                <br>

                Almacenar información: Los discos duros, SSD y otros dispositivos de almacenamiento guardan datos de
                forma permanente para que puedan ser accedidos cuando sea necesario.
                <br>

                Interactuar con el usuario: Los periféricos como teclados, ratones, pantallas táctiles y micrófonos
                permiten la interacción entre el usuario y la máquina.
                <br>

                Procesamiento gráfico: Las tarjetas gráficas (GPU) se encargan de procesar y mostrar gráficos,
                esenciales para aplicaciones de diseño, videojuegos y reproducción de contenido multimedia.
                <br>

                Comunicación y conectividad: Los puertos de red, Wi-Fi, Bluetooth y otros tipos de conexiones
                permiten
                la comunicación y transferencia de datos entre dispositivos.
                <br>

                Ejecutar sistemas operativos: Los sistemas operativos, como Windows, macOS, Linux, iOS, Android,
                entre
                otros, dependen del hardware para ejecutarse y coordinar el funcionamiento de todo el sistema.
            </p>
            <br>

            <p><strong> Componentes claves del Hardware</strong>
                <br>

                1.- CPU (Unidad Central de Procesamiento):

                Núcleos y Velocidad: Los procesadores modernos tienen varios núcleos que realizan tareas
                simultáneamente. La velocidad del reloj (medida en GHz) indica cuán rápido funciona cada núcleo.
                Arquitectura: Puede ser x86, x64, ARM, entre otros, y define cómo se ejecutan las instrucciones.
                <br>

                2.- RAM (Memoria de Acceso Aleatorio):

                Capacidad y Velocidad: Se mide en gigabytes (GB) y tiene diferentes velocidades de transferencia,
                como
                DDR3, DDR4, etc.
                Latencia: Tiempo que tarda en responder la memoria a las solicitudes de la CPU.
                <br>

                3.- Almacenamiento (Disco Duro o SSD):

                Capacidad: Mide la cantidad de datos que se pueden almacenar, usualmente en gigabytes (GB) o
                terabytes
                (TB).
                Velocidad de Lectura/Escritura: Los SSD son más rápidos que los discos duros tradicionales.
                <br>

                4.- Tarjeta Gráfica (GPU):

                VRAM: Memoria dedicada para procesar gráficos.
                Núcleos de Procesamiento: Similar a los núcleos de la CPU pero dedicados a tareas gráficas.
                <br>

                5.- Placa Base:

                Conectores: Determina qué dispositivos se pueden conectar (USB, HDMI, SATA, etc.).
                Chipset: Controla la comunicación entre la CPU y otros componentes.
                <br>

                6.- Periféricos:

                Tipos: Varían desde dispositivos de entrada como teclados y ratones hasta dispositivos de salida
                como monitores y altavoces.
                Conexiones: Puertos USB, inalámbricos (Bluetooth, Wi-Fi), etc.
                <br>


            </p>
